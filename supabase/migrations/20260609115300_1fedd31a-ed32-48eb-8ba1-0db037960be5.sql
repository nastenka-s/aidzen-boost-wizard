
-- Очередь тем
CREATE TABLE public.blog_topics (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  topic text NOT NULL,
  keywords text NOT NULL,
  slug_hint text NOT NULL,
  category text NOT NULL,
  priority int NOT NULL DEFAULT 100,
  status text NOT NULL DEFAULT 'pending',
  published_url text,
  generated_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now()
);

GRANT ALL ON public.blog_topics TO service_role;
ALTER TABLE public.blog_topics ENABLE ROW LEVEL SECURITY;
-- No public policies: only service_role from edge functions can access.

CREATE INDEX idx_blog_topics_status_priority ON public.blog_topics (status, priority, created_at);

-- История генераций
CREATE TABLE public.blog_generations (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  topic_id uuid REFERENCES public.blog_topics(id) ON DELETE SET NULL,
  status text NOT NULL,
  error_message text,
  commit_sha text,
  commit_url text,
  slug text,
  title text,
  created_at timestamptz NOT NULL DEFAULT now()
);

GRANT ALL ON public.blog_generations TO service_role;
ALTER TABLE public.blog_generations ENABLE ROW LEVEL SECURITY;
